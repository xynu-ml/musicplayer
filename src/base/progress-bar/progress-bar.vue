<template>
	<div class="progress-bar" ref="progressBar" @click="progressClick">
		<div class="bar-inner">
			<div class="progress" ref="progress"></div>
			<div class="progress-btn-wrapper" ref="btnWrapper" @touchstart.prevent="progressTouchStart"  @touchmove.prevent="progressTouchMove" @touchend.prevent="progressTouchEnd">
				<div class="progress-btn"></div>
			</div>
		</div>
	</div>
</template>

<script>
	import {prefixStyle} from '@/common/js/dom'
	
	const barBtnWidth=16
	const transform=prefixStyle('transform')
	export default {
		props:{
			percent:{
				type: Number,
                default: 0
			}
		},
		created(){
			this.touch={}
		},
		methods:{
			progressTouchStart(e){
				this.touch.initiated=true
				this.touch.startX=e.touches[0].pageX
				this.touch.left=this.$refs.progress.clientWidth
			},
			progressTouchMove(e){
				if(!this.touch.initiated){
					return 
				}
				const delX=e.touches[0].pageX-this.touch.startX
				const offsetWidth=Math.min(Math.max(0,this.touch.left+delX),this.$refs.progressBar.clientWidth-barBtnWidth)
				this._offset(offsetWidth)
			},
			progressTouchEnd(){
				this.touch.initiated = false
				this._trigglePercent()
			},
			progressClick(e){
				const rect=this.$refs.progressBar.getBoundingClientRect()
				const offsetWidth=e.pageX-rect.left
				this._offset(offsetWidth)
				this._trigglePercent()
			},
			_trigglePercent(){
				const barWidth = this.$refs.progressBar.clientWidth-barBtnWidth 
				const percent=this.$refs.progress.clientWidth/barWidth
				this.$emit("percentChange",percent)
			},
			_offset(offsetWidth){
				this.$refs.progress.style.width=`${offsetWidth}px`
				this.$refs.btnWrapper.style[transform]=`translate3d(${offsetWidth}px,0,0)`
			}
		},
		watch:{
			percent(newVal){
				if(newVal>=0&&!this.touch.initiated){
					const progressWidth=this.$refs.progressBar.clientWidth-barBtnWidth
					const offsetWidth=progressWidth*newVal
					this._offset(offsetWidth)
				}
				
			}
		}
	}
</script>

